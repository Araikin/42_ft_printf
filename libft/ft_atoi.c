#include "libft.h"

int		ft_atoi(const char *str)
{
	int sign;
	int n;
	int i;

	sign = 1;
	n = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + str[i] - '0';
		i++;
	}
	return (sign * n);
}
